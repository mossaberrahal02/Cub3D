#include "gnl.h"

int	ft_strchr2(const char *s, int c)
{
	size_t	i;

	i = 0;
	if (!s)
		return (0);
	while (s[i])
	{
		if ((char)c == s[i])
			return (1);
		i++;
	}
	return (0);
}
